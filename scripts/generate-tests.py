def generate_typescript_setup() -> str:
    """Generate a setup.ts file with shared test utilities for TypeScript rules."""
    return textwrap.dedent("""\
    // Generated by Whetstone — do not edit manually
    // Shared test utilities for Whetstone TypeScript eval tests

    import * as fs from 'fs';
    import * as path from 'path';
    import { globSync } from 'glob';

    /**
     * Discover all TypeScript files in the project source directory.
     * Excludes whetstone-generated files and node_modules.
     */
    export function findSourceFiles(
      baseDir: string = 'src',
      extensions: string[] = ['ts', 'tsx']
    ): string[] {
      const pattern = `${baseDir}/**/*.{${extensions.join(',')}}`;
      return globSync(pattern).filter(
        (f: string) => !f.includes('whetstone/') && !f.includes('node_modules/')
      );
    }

    /**
     * Read a file and split into lines for line-by-line analysis.
     */
    export function readLines(filepath: string): string[] {
      try {
        return fs.readFileSync(filepath, 'utf-8').split('\\n');
      } catch {
        return [];
      }
    }

    /**
     * Format a violation message with file path and line number.
     */
    export function violation(filepath: string, line: number, message: string): string {
      return `${filepath}:${line} - ${message}`;
    }
    """)



# --- Rust test generation ---


def _generate_rs_use_check(sig_desc: str, indent: str) -> str:
    """Generate a Rust use statement check from a signal description."""
    use_match = re.search(r"use\s+[`'\"]?(\w+(?:::\w+)*)[`'\"]?", sig_desc)
    if use_match:
        path = use_match.group(1)
        return textwrap.dedent(f"""\
{indent}// Check for 'use {path}' statement
{indent}for (i, line) in content.lines().enumerate() {{
{indent}    let trimmed = line.trim();
{indent}    if trimmed.starts_with("use ") && trimmed.contains("{path}") {{
{indent}        violations.push(format!(
{indent}            "{{}}:{{}} - uses {path}",
{indent}            filepath, i + 1
{indent}        ));
{indent}    }}
{indent}}}""")
    return ""


def _generate_rs_signal_check(signal: dict, dep_name: str) -> str:
    """Generate a Rust signal check from a signal definition."""
    strategy = signal.get("strategy", "")
    sig_desc = signal.get("description", "")
    sig_id = signal.get("id", "unknown")
    weight = signal.get("weight", "moderate")
    desc_lower = sig_desc.lower()
    INDENT = "            "  # 12 spaces — inside for loop body

    # Add signal identifier comment
    prefix = f"{INDENT}// Signal: {sig_id} ({strategy}, {weight})\n"

    if strategy == "ai":
        return f"{prefix}{INDENT}// AI signal (not auto-testable): {sig_desc}"

    if strategy == "lint_proxy":
        return f"{prefix}{INDENT}// lint_proxy signal (handled by clippy config): {sig_desc}"

    # Pattern-based checks
    if strategy == "pattern":
        # Use statement check
        if "use " in desc_lower or "use::" in sig_desc:
            use_check = _generate_rs_use_check(sig_desc, INDENT)
            if use_check:
                return prefix + use_check

        # Try to extract a deprecated function/method name
        deprecated_match = re.search(
            r"(?:uses?|calls?|invokes?)\s+[`'\"]?(\w+(?:::\w+)*(?:\(\))?)[`'\"]?",
            sig_desc,
        )
        if deprecated_match or "deprecated" in desc_lower:
            target = deprecated_match.group(1) if deprecated_match else ""
            if target:
                return prefix + textwrap.dedent(f"""\
{INDENT}for (i, line) in content.lines().enumerate() {{
{INDENT}    if line.contains("{target}") {{
{INDENT}        violations.push(format!(
{INDENT}            "{{}}:{{}} - uses deprecated {target}",
{INDENT}            filepath, i + 1
{INDENT}        ));
{INDENT}    }}
{INDENT}}}""")

        # Generic pattern: try to extract a token
        token_match = re.search(r"[`'\"]([^`'\"]+)[`'\"]", sig_desc)
        if token_match:
            token = token_match.group(1)
            return prefix + textwrap.dedent(f"""\
{INDENT}for (i, line) in content.lines().enumerate() {{
{INDENT}    if line.contains("{token}") {{
{INDENT}        violations.push(format!(
{INDENT}            "{{}}:{{}} - matches pattern: {token}",
{INDENT}            filepath, i + 1
{INDENT}        ));
{INDENT}    }}
{INDENT}}}""")

    # AST-based checks — generate string matching approximations
    if strategy == "ast":
        # Use statement check (also valid in ast strategy)
        if "use " in desc_lower or "use::" in sig_desc:
            use_check = _generate_rs_use_check(sig_desc, INDENT)
            if use_check:
                return prefix + use_check

        # Unsafe block detection
        if "unsafe" in desc_lower:
            return prefix + textwrap.dedent(f"""\
{INDENT}for (i, line) in content.lines().enumerate() {{
{INDENT}    let trimmed = line.trim();
{INDENT}    if trimmed.starts_with("unsafe ") || trimmed.contains("unsafe {{") {{
{INDENT}        violations.push(format!(
{INDENT}            "{{}}:{{}} - uses unsafe block",
{INDENT}            filepath, i + 1
{INDENT}        ));
{INDENT}    }}
{INDENT}}}""")

        # Unwrap detection
        if "unwrap" in desc_lower or "panic" in desc_lower:
            return prefix + textwrap.dedent(f"""\
{INDENT}for (i, line) in content.lines().enumerate() {{
{INDENT}    if line.contains(".unwrap()") && !line.trim().starts_with("//") {{
{INDENT}        violations.push(format!(
{INDENT}            "{{}}:{{}} - uses .unwrap() which may panic",
{INDENT}            filepath, i + 1
{INDENT}        ));
{INDENT}    }}
{INDENT}}}""")

        # Deprecated function call
        deprecated_match = re.search(
            r"(?:uses?|calls?)\s+[`'\"]?(\w+(?:::\w+)*)[`'\"]?", sig_desc
        )
        if deprecated_match or "deprecated" in desc_lower:
            target = deprecated_match.group(1) if deprecated_match else ""
            if target:
                short = target.split("::")[-1]
                return prefix + textwrap.dedent(f"""\
{INDENT}for (i, line) in content.lines().enumerate() {{
{INDENT}    if line.contains("{short}") && !line.trim().starts_with("//") {{
{INDENT}        violations.push(format!(
{INDENT}            "{{}}:{{}} - uses deprecated {target}",
{INDENT}            filepath, i + 1
{INDENT}        ));
{INDENT}    }}
{INDENT}}}""")

    # Fallback — for complex AST checks, suggest Dylint
    return f"{prefix}{INDENT}// TODO: Implement {strategy} check for: {sig_desc}\n{INDENT}// For complex AST analysis in Rust, consider Dylint: https://github.com/trailofbits/dylint"


def generate_rust_test(rule: dict) -> str:
    """Generate a cargo test file for a Rust rule."""
    rule_id = rule.get("id", "unknown")
    source_url = rule.get("source_url", "")
    dep_name = rule.get("_dep_name", "unknown")
    description = rule.get("description", "").strip()
    func_name = _sanitize_name(rule_id)
    signals = rule.get("signals", [])

    # Build signal checks first to determine if we have real checks
    signal_checks: list[str] = []
    has_real_check = False
    for signal in signals:
        check = _generate_rs_signal_check(signal, dep_name)
        signal_checks.append(check)
        if (
            "TODO" not in check
            and "AI signal" not in check
            and "lint_proxy" not in check
        ):
            has_real_check = True

    header = _test_header_rs(rule_id, source_url, has_real_check=has_real_check)

    checks = (
        "\n\n".join(signal_checks)
        if signal_checks
        else "            // TODO: Implement signal checks"
    )
    todo_footer = (
        ""
        if has_real_check
        else "\n\n            // TODO: Add content scanning logic based on signals above"
    )

    return f"""{header}

#[cfg(test)]
mod {func_name}_tests {{
    use std::fs;
    use std::path::Path;

    fn find_rs_files(dir: &str) -> Vec<String> {{
        let mut files = Vec::new();
        if let Ok(entries) = fs::read_dir(dir) {{
            for entry in entries.flatten() {{
                let path = entry.path();
                if path.is_dir() && !path.to_str().unwrap_or("").contains("whetstone") {{
                    files.extend(find_rs_files(path.to_str().unwrap_or("")));
                }} else if path.extension().map_or(false, |e| e == "rs") {{
                    if let Some(p) = path.to_str() {{
                        files.push(p.to_string());
                    }}
                }}
            }}
        }}
        files
    }}

    #[test]
    fn test_{func_name}() {{
        let mut violations: Vec<String> = Vec::new();
        let files = find_rs_files("src");

        for filepath in &files {{
            let content = match fs::read_to_string(filepath) {{
                Ok(c) => c,
                Err(_) => continue,
            }};

{checks}{todo_footer}
        }}

        assert!(
            violations.is_empty(),
            "{{}} violation(s) found for {rule_id}:\\n{{}}",
            violations.len(),
            violations.join("\\n")
        );
    }}
}}
"""


