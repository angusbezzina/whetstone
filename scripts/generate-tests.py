def generate_ruff_config(rules: list[dict]) -> str:
    """Generate ruff.whetstone.toml overlay."""
    lines = [
        "# Generated by Whetstone — do not edit manually",
        "# Ruff configuration overlay for Whetstone rules",
        f"# Last updated: {datetime.now(timezone.utc).strftime('%Y-%m-%d')}",
        "",
        "[lint]",
    ]

    select_rules: list[str] = []
    for rule in rules:
        for signal in rule.get("signals", []):
            if signal.get("strategy") == "lint_proxy":
                # Try to extract ruff rule code from description
                desc = signal.get("description", "")
                match = re.search(r"\b([A-Z]+\d{3,4})\b", desc)
                if match:
                    select_rules.append(match.group(1))

    if select_rules:
        rules_str = ", ".join(f'"{r}"' for r in sorted(set(select_rules)))
        lines.append(f"extend-select = [{rules_str}]")
    else:
        lines.append("# No lint_proxy signals found in rules")
        lines.append("# extend-select = []")

    lines.append("")
    return "\n".join(lines)


def _categorize_biome_rule(rule_name: str) -> str:
    """Categorize a biome rule into its group (suspicious, correctness, style, etc.)."""
    # Rules starting with 'no' that indicate suspicious code
    suspicious_rules = {
        "noExplicitAny", "noDebugger", "noConsoleLog", "noDoubleEquals",
        "noShadowRestrictedNames", "noImplicitAnyLet", "noArrayIndexKey",
        "noAssignInExpressions", "noConfusingVoidType",
    }
    # Rules about correctness
    correctness_rules = {
        "noUnusedVariables", "noUnusedImports", "noUndeclaredVariables",
        "noInvalidConstructorSuper", "noConstAssign", "noEmptyPattern",
        "noNewSymbol", "noUnsafeFinally", "noVoidTypeReturn",
        "noChildrenProp", "noRenderReturnValue",
    }
    # Rules about style
    style_rules = {
        "useConst", "useTemplate", "useSelfClosingElements",
        "useImportType", "useEnumInitializers", "useExponentiationOperator",
        "useNumericLiterals", "useSingleVarDeclarator",
    }
    if rule_name in suspicious_rules:
        return "suspicious"
    if rule_name in correctness_rules:
        return "correctness"
    if rule_name in style_rules:
        return "style"
    # Default: suspicious for 'no' rules, style for 'use' rules
    if rule_name.startswith("no"):
        return "suspicious"
    if rule_name.startswith("use"):
        return "style"
    return "suspicious"


def generate_biome_config(rules: list[dict]) -> dict:
    """Generate biome.whetstone.json overlay with proper nested group structure."""
    # Collect rules by biome group
    grouped: dict[str, dict[str, str]] = {}

    for rule in rules:
        for signal in rule.get("signals", []):
            if signal.get("strategy") == "lint_proxy":
                desc = signal.get("description", "")
                # Try to extract biome rule name
                match = re.search(r"\b(no\w+|use\w+)\b", desc)
                if match:
                    rule_name = match.group(1)
                    group = _categorize_biome_rule(rule_name)
                    if group not in grouped:
                        grouped[group] = {}
                    grouped[group][rule_name] = "error"

    config: dict = {
        "$schema": "https://biomejs.dev/schemas/1.9.4/schema.json",
        "_comment": "Generated by Whetstone — do not edit manually",
        "linter": {
            "enabled": True,
            "rules": grouped if grouped else {},
        },
    }

    return config


def _categorize_clippy_lint(lint_name: str) -> str:
    """Categorize a clippy lint into its group (Pedantic, Correctness, Restriction, etc.)."""
    # Well-known pedantic lints
    pedantic_lints = {
        "doc_markdown", "missing_errors_doc", "missing_panics_doc",
        "must_use_candidate", "module_name_repetitions", "redundant_closure_for_method_calls",
        "needless_pass_by_value", "trivially_copy_pass_by_ref", "too_many_lines",
    }
    # Well-known correctness lints
    correctness_lints = {
        "absurd_extreme_comparisons", "erasing_op", "almost_swapped",
        "approx_constant", "deprecated_cfg_attr", "misrefactored_assign_op",
    }
    # Well-known restriction lints
    restriction_lints = {
        "unwrap_used", "expect_used", "print_stdout", "print_stderr",
        "dbg_macro", "todo", "unimplemented", "panic", "unreachable",
        "indexing_slicing", "as_conversions", "shadow_reuse",
    }
    if lint_name in pedantic_lints:
        return "Pedantic"
    if lint_name in correctness_lints:
        return "Correctness"
    if lint_name in restriction_lints:
        return "Restriction"
    return "Style"


def generate_clippy_config(rules: list[dict]) -> str:
    """Generate clippy.whetstone.toml overlay with lint categories."""
    lines = [
        "# Generated by Whetstone — do not edit manually",
        "# Clippy configuration overlay for Whetstone rules",
        f"# Last updated: {datetime.now(timezone.utc).strftime('%Y-%m-%d')}",
        "",
        "[lints.clippy]",
    ]

    # Collect lints grouped by category
    categorized: dict[str, list[tuple[str, str]]] = {}

    for rule in rules:
        for signal in rule.get("signals", []):
            if signal.get("strategy") == "lint_proxy":
                desc = signal.get("description", "")
                # Try to extract clippy lint names (snake_case identifiers)
                match = re.search(r"\b([a-z][a-z0-9_]+)\b", desc)
                if match:
                    lint_name = match.group(1)
                    # Determine severity: 'deny' for restriction/correctness, 'warn' for pedantic/style
                    category = _categorize_clippy_lint(lint_name)
                    if category in ("Restriction", "Correctness"):
                        level = "deny"
                    else:
                        level = "warn"
                    if category not in categorized:
                        categorized[category] = []
                    categorized[category].append((lint_name, level))

    if categorized:
        for category in sorted(categorized.keys()):
            lines.append(f"# {category}")
            for lint_name, level in sorted(categorized[category]):
                lines.append(f'{lint_name} = "{level}"')
        lines.append("")
    else:
        lines.append("# No lint_proxy signals found in rules")
        lines.append("")

    return "\n".join(lines)


# --- Main orchestration ---


def generate_tests(
    project_dir: Path,
    rules_dir: Path | None = None,
    output_dir: Path | None = None,
    lint_dir: Path | None = None,
    lang_filter: str | None = None,
    dry_run: bool = False,
) -> dict:
    """Main test generation logic."""
    if rules_dir is None:
        rules_dir = project_dir / "whetstone" / "rules"
    if output_dir is None:
        output_dir = project_dir / "whetstone" / "evals"
    if lint_dir is None:
        lint_dir = project_dir / "whetstone" / "lint"

    rules, warnings, skipped = load_rules(rules_dir, lang_filter)

    if not rules:
        return {
            "generated": {
                "tests": {"python": [], "typescript": [], "rust": []},
                "lint_configs": [],
            },
            "rules_processed": 0,
            "skipped_unapproved": skipped,
            "warnings": warnings or ["No approved rules found"],
            "next_command": "Extract rules first: run whetstone doctor",
        }

    # Group by language
    by_lang: dict[str, list[dict]] = {"python": [], "typescript": [], "rust": []}
    for rule in rules:
        lang = rule.get("_language", "")
        if lang in by_lang:
            by_lang[lang].append(rule)

    generated_tests: dict[str, list[str]] = {"python": [], "typescript": [], "rust": []}
    generated_lint: list[str] = []

    # --- Python ---
    if by_lang["python"]:
        py_dir = output_dir / "python"
        if not dry_run:
            py_dir.mkdir(parents=True, exist_ok=True)

        for rule in by_lang["python"]:
            rule_id = rule.get("id", "unknown")
            fname = f"test_{_sanitize_name(rule_id)}.py"
            fpath = py_dir / fname

            content = generate_python_test(rule)
            if not dry_run:
                fpath.write_text(content)
            generated_tests["python"].append(str(fpath))

        # conftest.py
        conftest_path = py_dir / "conftest.py"
        if not dry_run:
            conftest_path.write_text(generate_python_conftest(py_dir))

        # Ruff config
        if not dry_run:
            lint_dir.mkdir(parents=True, exist_ok=True)
        ruff_path = lint_dir / "ruff.whetstone.toml"
        ruff_content = generate_ruff_config(by_lang["python"])
        if not dry_run:
            ruff_path.write_text(ruff_content)
        generated_lint.append(str(ruff_path))

    # --- TypeScript ---
    if by_lang["typescript"]:
        print(
            "  [experimental] TypeScript test generation produces signal scaffolds "
            "that may need manual implementation.",
            file=sys.stderr,
        )
        ts_dir = output_dir / "typescript"
        if not dry_run:
            ts_dir.mkdir(parents=True, exist_ok=True)

        for rule in by_lang["typescript"]:
            rule_id = rule.get("id", "unknown")
            fname = f"{rule_id.replace('.', '-')}.test.ts"
            fpath = ts_dir / fname

            content = generate_typescript_test(rule)
            if not dry_run:
                fpath.write_text(content)
            generated_tests["typescript"].append(str(fpath))

        # Generate setup.ts with shared test utilities
        setup_path = ts_dir / "setup.ts"
        if not dry_run:
            setup_path.write_text(generate_typescript_setup())

        # Biome config
        if not dry_run:
            lint_dir.mkdir(parents=True, exist_ok=True)
        biome_path = lint_dir / "biome.whetstone.json"
        biome_config = generate_biome_config(by_lang["typescript"])
        if not dry_run:
            biome_path.write_text(json.dumps(biome_config, indent=2) + "\n")
        generated_lint.append(str(biome_path))

    # --- Rust ---
    if by_lang["rust"]:
        print(
            "  [experimental] Rust test generation produces signal scaffolds "
            "that may need manual implementation.",
            file=sys.stderr,
        )
        rs_dir = output_dir / "rust"
        if not dry_run:
            rs_dir.mkdir(parents=True, exist_ok=True)

        for rule in by_lang["rust"]:
            rule_id = rule.get("id", "unknown")
            fname = f"whetstone_{_sanitize_name(rule_id)}.rs"
            fpath = rs_dir / fname

            content = generate_rust_test(rule)
            if not dry_run:
                fpath.write_text(content)
            generated_tests["rust"].append(str(fpath))

        # Clippy config
        if not dry_run:
            lint_dir.mkdir(parents=True, exist_ok=True)
        clippy_path = lint_dir / "clippy.whetstone.toml"
        clippy_content = generate_clippy_config(by_lang["rust"])
        if not dry_run:
            clippy_path.write_text(clippy_content)
        generated_lint.append(str(clippy_path))

    # Build appropriate next command based on what was generated
    test_paths = []
    if "python" in by_lang:
        test_paths.append("pytest whetstone/evals/python/")
    if "typescript" in by_lang:
        test_paths.append("npx vitest run whetstone/evals/typescript/")
    if "rust" in by_lang:
        test_paths.append("cargo test --test whetstone")

    if test_paths:
        next_command = "Run tests: " + " && ".join(test_paths)
    else:
        next_command = "Check status: python3 scripts/status.py --project-dir ."

    result: dict = {
        "generated": {
            "tests": generated_tests,
            "lint_configs": generated_lint,
        },
        "rules_processed": len(rules),
        "skipped_unapproved": skipped,
        "next_command": next_command,
    }
    if warnings:
        result["warnings"] = warnings

    return result


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate test files and linter configs from approved Whetstone rules."
    )
    parser.add_argument(
        "--project-dir",
        type=Path,
        default=Path("."),
        help="Project root directory (default: .)",
    )
    parser.add_argument(
        "--rules-dir",
        type=Path,
        help="Rules directory (default: whetstone/rules)",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        help="Test output directory (default: whetstone/evals)",
    )
    parser.add_argument(
        "--lint-dir",
        type=Path,
        help="Lint config output directory (default: whetstone/lint)",
    )
    parser.add_argument(
        "--lang",
        choices=["python", "typescript", "rust"],
        help="Only generate for specified language",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print what would be generated without writing files",
    )
    args = parser.parse_args()

    try:
        result = generate_tests(
            project_dir=args.project_dir,
            rules_dir=args.rules_dir,
            output_dir=args.output_dir,
            lint_dir=args.lint_dir,
            lang_filter=args.lang,
            dry_run=args.dry_run,
        )

        json.dump(result, sys.stdout, indent=2)
        sys.stdout.write("\n")

    except Exception as e:
        json.dump(
            {
                "error": str(e),
                "next_command": "Check rules directory and PyYAML installation",
            },
            sys.stdout,
            indent=2,
        )
        sys.stdout.write("\n")
        sys.exit(1)


if __name__ == "__main__":
    main()
